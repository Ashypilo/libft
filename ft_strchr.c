char	*ft_strchr(const char *s1, int n)
{
	int i;

	i = 0;
	while (s1[i])
	{
		if (s1[i] == n)
			return ((char*)s1 + i);
		i++;
	}
	if (n == '\0')
		return ((char*)s1 + i);
	return (0);
}
